#!/bin/bash

echo "=========================================================="
echo "====================== bandit scan ======================="
echo "=========================================================="
bandit -r -c 'bandit.config' .
echo "=========================================================="


echo "=========================================================="
echo "====================== PEP 8 ERRORS ======================"
echo "=========================================================="
